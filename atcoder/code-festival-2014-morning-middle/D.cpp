    int M = *max_element(ALL(z))+1;
    vector<SegmentTree<nodeSumPointAdd<modint>>> dp(2,SegmentTree<nodeSumPointAdd<modint>>(M,0));
    dp[0].update(0,1);
    for(int i = 1; i <= N; ++i) {
        int nl = mp[p[i]-c[i]], nr = mp[p[i]+c[i]];
        int pl = mp[p[i-1]-c[i-1]], pr = mp[p[i-1]+c[i-1]];
        for(int j = nl; j <= nr; ++j) dp[i%2].update(j,dp[(i-1)%2].get(0,j));
        for(int j = pl; j <= pr; ++j) dp[(i-1)%2].update(j,-dp[(i-1)%2].get(j,j+1));
    }
