void solve() {
    ll A,B; cin >> A >> B;
    int m=0;
    for(int i=0;i<60;++i) {
        if(!((A>>i)&1) && ((B>>i)&1)) m=i;
    }
    vector<ll> ans;
    for(int i=0;i<m;++i) {
        if(((A>>i)&1)) {
            A += (1LL<<i);
            ans.push_back(1LL<<i);
        }
    }
    for(int i=m;0<=i;--i) {
        if(!((A>>i)&1LL) && ((B>>i)&1LL)) {
            A += (1LL<<i);
            ans.push_back(1LL<<i);
        }
    }
    cout << ans.size() << endl;
    for(auto& e:ans) cout << e << " "; cout << endl;
}

int main() {
    cin.tie(0);ios::sync_with_stdio(false);
    int T; cin >> T;
    while(T--) {
        solve();
    }
    return 0;
}
